    private void limpiarCampos(){
        txt_nickname.setText("");
        txt_passwd.setText("");
    }
    
    private void btn_registrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registrarActionPerformed
        nickname = txt_nickname.getText();
        contrasena = txt_passwd.getText();
              
        Boolean usuarioExiste = false;
     
        for (Jugador j : ProyectoPOO.listaJugadores) {
            if(j.getNombre().equals(nickname)){
                usuarioExiste = true;
            }
        }
        
        if(usuarioExiste){
            JOptionPane.showMessageDialog(null, "El usuario ya existe");
        }else if(nickname.equals("")||contrasena.equals("")){
            JOptionPane.showMessageDialog(null, "Rellene todos los campos");
        }else{
            limpiarCampos();
            ProyectoPOO.listaJugadores.add(new Jugador(nickname,contrasena));
            ProyectoPOO.guardarDatos();
            JOptionPane.showMessageDialog(null, "Usuario Creado");
        }
        
        
        // [borrar esto] Prueba para comprobar si se estan agregando correctamente
        ProyectoPOO.listaJugadores.forEach((jugador) -> System.out.println(jugador.getNombre()));
        
    }//GEN-LAST:event_btn_registrarActionPerformed

